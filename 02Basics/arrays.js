
// datatype - Array - array uses const

const superHeroes = ['Iron Man', 'Spiderman', 'Capt. America', 'Thor']

console.log(superHeroes);
console.log(superHeroes[0]); /** Get first element of the array */
console.log(superHeroes.length); /** Get the length of the array */
console.log(superHeroes[superHeroes.length - 1]) /** Get the last element of the array - first value starts with 0 and last value will be by 1 left i.e -1 */

console.log(`We have ${superHeroes.length} super heroes`); /** get length of the array */

console.log(`We have ${superHeroes[superHeroes.length - 1]} super heroes`); /** get the last super hero */

